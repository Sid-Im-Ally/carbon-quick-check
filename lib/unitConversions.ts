export function m2ToHectares(m2: number): number {
  return m2 / 10000;
}

export function hectaresToM2(ha: number): number {
  return ha * 10000;
}

export function kgToTonnes(kg: number): number {
  return kg / 1000;
}

export function tonnesToKg(tonnes: number): number {
  return tonnes * 1000;
}

export function kwhToMwh(kwh: number): number {
  return kwh / 1000;
}
